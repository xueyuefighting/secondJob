package com.bjdreamtech.dmt.util;

public class DMTStringUtil {
	
	
	/**
	 * 空值转换
	 * @param str
	 * @return
	 */
	public static String chgNull(String str){
		
		if(str==null){
			return "";
		}if("".equals(str)){
			return "";
		}if("null".equals(str)){
			return "";
		}
		
		return str;
	}
	
	public static void main(String [] args){
		
		System.out.println(chgNull(""));
		
		
	}
	
}
