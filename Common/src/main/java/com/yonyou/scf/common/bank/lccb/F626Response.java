package com.yonyou.scf.common.bank.lccb;

import java.util.ArrayList;
import java.util.List;

public class F626Response {
	/**
	 * 商户号
	 */
	private String busiNum;
	
	/**
	 * 笔数
	 */
	private Long totalNum;
	
	/**
	 * 循环域
	 */
	private List<F626Response_DataList> dataList = new ArrayList<F626Response_DataList>();

	public String getBusiNum() {
		return busiNum;
	}

	public Long getTotalNum() {
		return totalNum;
	}

	public List<F626Response_DataList> getDataList() {
		return dataList;
	}

	public void setBusiNum(String busiNum) {
		this.busiNum = busiNum;
	}

	public void setTotalNum(Long totalNum) {
		this.totalNum = totalNum;
	}

	public void setDataList(List<F626Response_DataList> dataList) {
		this.dataList = dataList;
	}

	@Override
	public String toString() {
		return "F626Response [busiNum=" + busiNum + ", totalNum=" + totalNum
				+ ", dataList=" + dataList + "]";
	}
	
}
